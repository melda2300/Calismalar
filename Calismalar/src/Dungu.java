public class Dungu {
    public static void dongu() {
        // Soru 7: Java'da bir döngü kullanarak 1'den 10'a kadar olan sayıları ekrana yazdıran bir program yazınız.
        for (int a = 1; a <= 10; a++) {
            System.out.println(a);
        }
    }

    public static void main( String[] args ) {
        dongu();

    }
}
